package com.muzikprojects;

public class Album {
    private int albumId;
    private String albumAdi;
    private int sarkiciID;


    public int getAlbumId() {
        return albumId;
    }

    public void setAlbumId(int albumId) {
        this.albumId = albumId;
    }

    public String getAlbumAdi() {
        return albumAdi;
    }

    public void setAlbumAdi(String albumAdi) {
        this.albumAdi = albumAdi;
    }

    public int getSarkiciID() {
        return sarkiciID;
    }

    public void setSarkiciID(int sarkiciID) {
        this.sarkiciID = sarkiciID;
    }
}
